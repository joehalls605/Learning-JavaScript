// Write a function that takes a decimal number as input and returns the number rounded to the nearest integer using Math.round().

function round(decimalNumber){
    return Math.round(decimalNumber);
}
round(0.8);

// Generate a random integer between 1 and 10 using Math.random().

function randomInteger(){
    return Math.floor(Math.random() * 10 + 1);
}
randomInteger();

// Given three numbers (25, 78, and 45), write a function that returns the highest number using Math.max().

function highestNumber(num1, num2, num3){
    return Math.max(num1, num2, num3);
}
highestNumber(25,78, 45);

// Power Calculation

function raisePower(num){
    return Math.pow(num, 3);
}
raisePower(5);

// Square Root Calculation

function squareRoot(num){
    return Math.sqrt(num);
}
squareRoot(16);

